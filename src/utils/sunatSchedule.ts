export const getUpcomingDeadlines = (): SunatDeadline[] => {
  const today = new Date();
  today.setHours(0,0,0,0);
  
  // Find all deadlines that are in the future or today
  const upcoming = sunatSchedule2026.filter(d => {
    const dDate = new Date(d.fecha);
    // Add 12 hours to avoid timezone issues parsing ISO strings
    dDate.setHours(12, 0, 0, 0); 
    return dDate >= today;
  });

  // Group by Period
  if (upcoming.length === 0) return [];
  
  // Return the closest Period's deadlines
  const firstPeriod = upcoming[0].periodo;
  return upcoming.filter(d => d.periodo === firstPeriod);
};
